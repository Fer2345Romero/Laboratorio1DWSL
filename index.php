        <div class="card shadow-lg">
            <h2>IF</h2>
            <p>En Kotlin, <code>if</code> puede ser usado como expresión que devuelve un valor.</p>
            <pre>
fun main() {
    val edad = 20
    if (edad >= 18) {
        println("Eres mayor de edad")
    } else {
        println("Eres menor de edad")
    }
}
            </pre>
        </div>

        <div class="card shadow-lg">
            <h2>WHEN (equivalente a switch)</h2>
            <p>La expresión <code>when</code> reemplaza a <code>switch</code> en Kotlin.</p>
            <pre>
fun main() {
    val color = "rojo"
    when (color) {
        "rojo" -> println("El color es rojo")
        "azul" -> println("El color es azul")
        else -> println("Color desconocido")
    }
}
            </pre>
        </div>

        <div class="card shadow-lg">
            <h2>WHILE</h2>
            <p><code>while</code> ejecuta un bloque de código mientras la condición sea verdadera.</p>
            <pre>
fun main() {
    var contador = 1
    while (contador <= 5) {
        println(contador)
        contador++
    }
}
            </pre>
        </div>

        <div class="card shadow-lg">
            <h2>FOR</h2>
            <p>El bucle <code>for</code> recorre rangos, colecciones o arrays.</p>
            <pre>
fun main() {
    for (i in 1..5) {
        println(i)
    }
}
            </pre>
        </div>

        <div class="card shadow-lg">
            <h2>FOREACH</h2>
            <p>La función <code>forEach</code> permite recorrer colecciones de forma más declarativa.</p>
            <pre>
fun main() {
    val frutas = listOf("Manzana", "Banana", "Cereza")
    frutas.forEach { fruta ->
        println(fruta)
    }
}
            </pre>
